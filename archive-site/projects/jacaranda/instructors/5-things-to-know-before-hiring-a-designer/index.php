					<h3>2. This site is not for you.</h3>
					<p>That might be a hard thing to hear, but it’s true. Paying to have a website made is like buying shoes for your kid. Yeah, it’s your money, but you don’t have to walk in them every day.</p>
					<p>At the end of the project, this website is for your customers. Believe it or not, there’s an entire industry around how a person uses a website, and there are millions of tests being run every day. <em>What should your button say? Should the colors compliment or contrast? How many steps is too many for a form? How much are they willing to read? (Hint: Not much. You aren't even reading this.)</em> My job is to research this stuff to make your site better.</p>
					<p>My advice is to trust the designer. You hired them for their expertise, so let them tell you no to your stupid requests. You might not agree with all the choices, but we know what’s compliant, what translates well across all devices, and what will get you results.</p>
				</div>
