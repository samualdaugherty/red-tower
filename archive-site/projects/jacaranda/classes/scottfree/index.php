					<h2>The Story</h2>
					<p>We were approached about doing the Scottfree Salon website by their marketing director, Jordan. Up until now, they were using a not-to-be-named online website builder, and we all know how shitty those are to use. They were constrained by the limits of the theme, and it didn't really match the brand at all. This was a huge problem, especially in an industry where image is everything. Jordan, along with Scottfree's Creative Director, Parker, came to us with a vision: clean, simple, minimal; but also bold and edgy. A little bit MOMA, a little bit punk rock. Now, who doesn't love some good punk rock?</p>
				</div>
			</div>

			<div class="row peepsp2">
