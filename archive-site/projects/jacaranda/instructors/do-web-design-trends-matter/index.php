					<h3>Scroll Animations - Maybe?</h3>
					<p>We used them. It’s little subtle movement on the page, like how things pop up into place as you scroll on this website (and then fade out of sight when you scroll back up to the top). Go ahead and scroll around, I can wait.</p>
					<p>This is one of those things that is really trendy, but not exactly necessary. It looks cool, for sure, but that’s not always the best reason to do something. The most important consideration for the average person: <strong>it costs more</strong>. It simply takes more time to create a site with animation than it does to create a site without it. So if it’s in your budget, go for it. People will definitely tell you how cool it is. But if you’re trying to get a website up, and keep costs down, go ahead and skip it.</p>
				</div>
			</div>
		</div>
