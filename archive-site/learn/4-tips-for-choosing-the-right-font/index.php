					<h3>3. Readability</h3>
					<p>What good is your message if it's hard to read? So readability should be at the top of the list when choosing a font. People rarely notice how readable a font is, but they always notice when it's <em>not</em>. We expect what we're looking at to be readable, so make sure to keep that top of mind. One way to check this is to use the "Il1" test, to see how easy it is to tell the difference between a capital I, a lowercase l, and the numeral 1.</p>
					<p>Keep in mind too, that white space matters. Having the right amount of space between letters and lines can make them more readable, and easier to digest. When in doubt, increase the size, and use nothing smaller than 12pt font. Remember that your users want to scan the text, not read it like a book. Combine readability with hierarchy, and you'll make their life easy. Scannable text should always be the goal.</p>
