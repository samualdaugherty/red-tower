					<h3>2. Limitations of Use</h3>
					<p>When you work using a site builder like Squarespace, you’re limited to what the template you chose can do. Want to move your menu below the header graphic instead of at the top? Too bad if your template doesn’t support that. When you’re working with a designer, and you’re giving them suggestions and feedback, they can do anything you need. Want to move the menu? No problem. Want to rearrange the photos in your slideshow? We can do it. Your site builder might not be able to.</p>
					<p>Here’s a real world example for you. Just recently I went to shop for some new wheels for my car, and stumbled upon a site built using WIX. It had all the social media icons across the bottom, but this company only had instagram, so the other buttons were broken, but still displayed. There was a section on the site for company phone number and address, but this was a web based company, with no phone number, and no physical address. So those were just blank. He couldn’t remove them, and was stuck with functionality on the page that he didn’t need.</p>
