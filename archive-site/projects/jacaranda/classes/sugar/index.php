						<h3>Concept</h3>
						<p class="small">What we came up with we like to describe as "realistic subtlety". Rarely will you find absolute white and absolute black in nature. It's always a million shades in between. So we settled on a very light gradient that appears to cast a natural shadow on the whole page, from barely grey to almost white. We don't use a solid black or a solid white anywhere on the site.</p>
						<p class="small">We then chose imagery that fell in that same vein. We took actual photographs of treats from the Sugar kitchen, and we lit them so that the natural highlights and shadows matched up with the natural gradient of the background. And then we kept it all to a stark minimum.</p>
