					<h3>Brutalism - It’s a thing</h3>
					<p>What is it? It’s a rebellion against “minimalism”. Think 90’s MTV graphics, and that’s pretty much what it is. Neon colors, multiple fonts, complicated designs. It’s the exact opposite of “clean” design. It’s a really hot trend right now for designers, but it’s really only impressing other designers. Most people aren’t going to be fans of it. It essentially throws all that we’re used to about websites out the window, and User Experience is an afterthought with brutalist designs.</p>
					<p>You can take some of the trends though, and roll with them, as long as they fit your brand. Bright colors, bold gradients, duo-tone graphics, are all hip and cool. They can work for your site, as long as you use them in a way that makes sense for your company. Remember, strengthening your brand is way more important than being cool. Trends are temporary. Your business shouldn’t be.</p>
				</div>
