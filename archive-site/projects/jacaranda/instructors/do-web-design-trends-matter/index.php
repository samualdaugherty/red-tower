					<p>The truth is, as much as I love making really trendy stuff, most of it doesn’t matter for the average business owner, or even the average website. Let’s look at a few of the trends for 2018, talk about why they do/don’t matter, and what you need to know about embracing them for your next website. We’ll start with some easy ones!</p>
