					<h3>Crazy Menus - Hold that thought.</h3>
					<p>Think of almost every site you’ve ever visited on your phone, and it has a “hamburger” on it. That’s the three lines that you click to open the menu. The hamburger has been around forever, and designers are trying to kill it in favor of new things, like continuous side-scrolling, or just <em>no menu at all</em>. Again, it’s fun and trendy, but it can create a poor user experience, and even confuse people who are simply used to using a conventional menu to navigate the site.</p>
					<p>If you want to implement a different sort of navigation, you can have fun, be different, and still stick to convention. Maybe that hamburger opens up a full-screen menu, with your contact info and social media links right in it? That’s fine. Have some fun with your site, but have a reason for doing it too. And never confuse your visitors.</p>
				</div>
			</div>
