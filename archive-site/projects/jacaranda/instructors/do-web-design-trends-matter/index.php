				<div class="col-sm-9">
					<h3>Bold Typography - Go for it.</h3>
					<p>This is something we’ve been seeing a lot this year, and I really like it. We used it on our site, if you needed more proof. Gone are the big, full-screen header graphics, and they’ve been replaced with big, bold, in-your-face headlines. It gets your attention, and you can’t get more concise than just saying <strong>“READ THIS”</strong>, right?</p>
					<p>Typography might sound boring, and to most non-designers, it generally is. I once watched a documentary about a font, and my wife thought I had lost my mind. I live for this shit. Why? Because fonts have character, and give you a feel for where you are, before you even read the words. Gothic, or serif, fonts (like our headlines) invoke stability and classicism. They’re timeless. Modern typography are super popular with tech startups, and tend to be edgy and fashionable. They’re sleek and modern. Script, or handwritten fonts are fancy and elegant. You see them in a lot of marketing targeting women. Fonts create a sense of style, regardless of the words they're creating.</p>
					<p>Font is so important, that many brands spend thousands of dollars having custom fonts designed for their company. Intel and AirBnB both did this. You don’t have to go that far, but this is how powerful typography can be.</p>
				</div>
			</div>
