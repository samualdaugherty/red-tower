					<h3>3. You have work to do.</h3>
					<p>You hired me for a reason. You looked at my website, my designs appealed to you, and you wanted my help. But, what you don’t see is that all of those sites required a lot of work from the client.</p>
					<p>Writing copy is the biggest time-suck for all of these projects. I don’t know everything about you and your company, so you have to write all of that stuff for me. I can proofread and edit it, but you have to take the time to think about what you want to say, and type it up before I can start.</p>
					<p>However, a lot of designers can’t even edit. Our best work is done sitting at a desk late at night, in our pajamas, with headphones on, and no awareness of the outside world.</p>
					<p>By nature, designers and developers are not exceptional communicators. If you aren’t either, consider hiring a copywriter to help you with your content. Actually, just hire a copywriter. You’re not as good at communicating as you think you are. But even then, you’re going to have to put some effort into what you want to say about your company.</p>
				</div>
			</div>
		</div>
