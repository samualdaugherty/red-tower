					<h3>Scalable Vector Graphics (SVG) - Use them.</h3>
					<p>SVG is not new, but we're seeing it used in new ways. They’re graphic files, like pictures, but these illustrations use a vector file that can be blown up to any size. And in a lot of web applications, it’s essentially plotted coordinates on a screen, that the browser draws. It’s using math, instead of photographs, and the file sizes are substantially smaller. This helps with load times, and load times are massively important these days. You don't want a slow site.</p>
					<p>The downside is that they aren’t supported outside of modern browsers. So, if you’re using an old version of Internet Explorer, they just won’t show up for you <em>(and also, stop using old version of Internet Explorer)</em>. But for every smartphone out there and computer released after 2010, they’ll work great. And we recommend them whenever possible. They cannot replace photographs, but for icons and graphics (like logos), they can go a long way.</p>
				</div>
			</div>
		</div>
	</div>

