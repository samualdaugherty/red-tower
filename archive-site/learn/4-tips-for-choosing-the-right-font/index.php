					<h3>1. Brand Consistency</h3>
					<p>Using the same font throughout all of your marketing pieces establishes harmony throughout the brand. It makes every piece you put out recognizable. And, just like color, it says a lot about the "personality" of your company. When used consistently, people will begin to associate that font with your brand, expanding recognition. This is why it’s important to pick a typeface that has the personality you want to convey.</p>
					<p>Take Cadillac for example. The iconic calligraphy in the logo creates a mood of luxury and elegance. Without even looking at the cars, we’ve already established a tone. Their use of unique typography helps set the tone for the brand that is perceived by the consumer and transferred to the product or service.</p>
					<p>You work hard to establish your brand, and the font you choose should be a part of that. Make sure it matches the tone of your business. We <a href="https://redtowerdigital.com/learn/do-web-design-trends-matter/" target="_blank">already covered</a> the different types of fonts, and how they affect personality, so I won't ramble on again. But just know that if you're not sure, ask a designer.</p>
