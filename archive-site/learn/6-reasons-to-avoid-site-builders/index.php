					<h3>1. Quality of the Design</h3>
					<p>Have you ever looked through the demo templates on any of the site builders? They’re beautiful. They have subtle animations, and everything is properly spaced, there’s the perfect amount of whitespace and balance. But when you build yours, it doesn’t have any of those things, and it doesn’t quite look as good as the demo did. It looks alright, but not quite what you were hoping for.</p>
