					<h3>4. Use them correctly</h3>
					<p>The rule of thumb is to keep it simple. Stick to two fonts, and make sure there's a lot of differentiation. You never want to use two fonts that look too much alike, because that gets weird. Like food and wine, your fonts should pair nicely. <a href="https://fontpair.co/" target="_blank">FontPair</a> has some nice examples to get you started, and give you some inspiration. A good, general rule is to use one font (typeface, size, weight, whatever) for headlines, and another for body text. But play around with it, and find the balance that works for you and your brand.</p>
					<p>When it comes to good use, keep big, bold, fancy typefaces for headlines, and stick to something clean and simple or your main text. This will help cover everything we've talked about already. And, when considering fonts, remember that one font, used correctly, can have a big impact. In the image below, you can see how something as simple as font weight and color can set a different tone completely.</p>
