					<h3>1. You don’t really know what you want.</h3>
					<p>I like to call this the <em>"I know what I hate when I see it"</em> problem.</p>
					<p>In your mind, you want clean and simple. But, when I deliver clean and simple, you think it’s not jazzy enough or something. Fine. Whatever. But this is where having a really good idea of what you want, upfront, with examples, is the best way to start. Because changing your mind halfway through the project is going to add hours&mdash;and dollars&mdash;to the final product. And no one wants that.</p>
					<p>My suggestion is to start Googling. Or Pinterest&hellip;ing (I just coined that phrase). Do something. Go out on the web and start looking at other sites. Don’t just save the good ones either. Having examples of what you <em>don’t like</em> is really helpful too.</p>
					<p>Don’t tell me that you like this site, or that site, tell me <em>what</em> you like about them. And tell me what you don’t! The best way for a visual person to know what you want, is to show them. This is why we have that awesome questionnaire for you to fill out before we ever start the project. This helps me get a feel for your tastes before we even start down this tumultuous path.</p>
				</div>
