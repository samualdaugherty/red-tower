					<h3>3. Best Practices</h3>
					<p>To understand internet best-practices, you have to understand how code works. You probably don’t, and I won’t bore you with it. But, there are standards for these. There are a lot of ways to build a site without following standards, and it will still work, but you’ll never get to the front page of Google, and it can hurt the experience your customers have when going to your site.</p>
					<p>Think of it this way: there are a lot of ways to wire up an outlet in your house. But there are standards for doing it right. If you don’t follow them, at best you just have a mess behind the walls. At worst, it can be disastrous. A website is no different. Site builders use a “What You See Is What You Get” editor (often called a WYSIWYG). They make things look like you want, but they create a mess of code behind the scenes that can make your site slow to load at best, and actually break things at worst, and cause your site to not work.</p>
