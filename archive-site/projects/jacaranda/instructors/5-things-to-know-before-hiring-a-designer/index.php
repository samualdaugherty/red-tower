					<h3>4. But not too much work.</h3>
					<p>This is a big one. There’s a fine line between being involved in the design, and trying to <em>be a designer</em>. This kind of goes along with point number two, but I want to reiterate it here. Just because you like that site you stumbled across doesn’t mean that it’s up to par with web standards. Plus, what you like might be considered bad design.</p>
					<p>Lastly, sites look different on different devices, whether it’s your laptop, phone or tablet. And, depending on the color correction of your monitor, or the gamut of colors it can display, the colors aren’t going to look the same on every computer in the world.</p>
					<p>If you don’t know what any of that means, you shouldn’t be playing designer on this project. I’m working <em>with</em> your company, not <em>for</em> your company. So, do us both a favor.</p>
