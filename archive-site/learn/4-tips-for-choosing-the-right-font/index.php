					<h3>2. Hierarchy</h3>
					<p>Put plainly, hierarchy guides your user, and shows them what you want them to look at. When used effectively, the typography establishes importance, and gives the page order. It gives you more control over what parts of the content viewers focus on first. Using a different  color or a bigger, bolder font will draw the viewers' attention, allowing you to be more creative with the design. This means that the most important information doesn’t have to be at the top of the page&mdash;if the message stands out, viewers will be drawn to that information first.</p>
					<p>Without hierarchy, then the entire content will fall flat, and create a visual "wall of text", that no one will read. Users want to be able to easily determine what matters on the page, and go there, and a lack of hierarchy will cause users to simply abandon ship. Good hierarchy has a variety sizes, colors, fonts, and typefaces to bring these differences to attention.</p>
