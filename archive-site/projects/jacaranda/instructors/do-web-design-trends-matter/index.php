					<h3>Video - Use it if it makes sense.</h3>
					<p>First off, <strong>video is expensive</strong>. And I don’t mean kind of expensive, I mean double-the-budget expensive. So that’s the main consideration for a lot of people, and you should have a good reason for spending that kind of cash. Secondly, you need to know why you want video.</p>
					<p>I’ve had customers ask for video, and full-screen video backgrounds, but they rarely know why they want it. Most of the time, they just think it looks cool. But that video is going to distract user attention away from the content too. So if the video isn’t complementing what you’re trying to say, then it’s doing more harm than good. Using video as a part of your marketing, especially on social media, is a really good way to increase engagement. But it should make sense why the video is there. Keep in mind that video files are huge too, and take a long time to load. That’s rarely a good thing, so make it count.</p>
