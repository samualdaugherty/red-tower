					<h2>Design</h2>
					<p>The final design featured big bold headlines, a lot of white space, and thin diagonal lines to separate sections. The lines all point down, giving the site a sense of flow and direction, and encouraging the user to continue scrolling. When you have long pages, you want to make sure people are moving down the page. And it's subtle, but all of the arrows point to the headline of the section below it. This was intentional.</p>
                    <p>For visual consistency, we set up a "photo day" for the salon. We hired a photographer, and had all of the photos taken on the same day. The salon has massive windows, so changes in weather or lighting can really effect the photos, and we wanted to try and avoid that. On a site this simple, something as basic as photo lighting can really stand out.</p>
