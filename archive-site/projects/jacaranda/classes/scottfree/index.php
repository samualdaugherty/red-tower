					<div class="col-md-7 col-sm-9 col-xs-11 pull-right process-text shadow">
						<h3>Concept</h3>
						<p class="small">Scottfree's new site was going to be incredibly minimal. As easy as that sounds to do, it actually creates a new set of obstacles when laying out the site. If you have an ultra-minimal design, with a lot of content, it can look like an imposing wall of text.<p>
						<p class="small">To avoid this, we broke all of the content into sections, and included ample amounts of white space to separate everything out. We then created a zig-zag pattern with the content where possible, to give the site a sense of flow, and encourage the user to continue reading.</p>
					</div>
				</div>
